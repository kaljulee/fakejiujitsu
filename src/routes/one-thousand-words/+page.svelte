# Shadows on the Wall of Kano’s Cave

Technique (I’m going to refer the entire library of BJJ techniques and positions as Technique) gets treated as an almost mystical fetish, like a physical spell that must be cast just right for the smaller person to defeat the larger.  When we encourage grapplers to focus on Technique, we are almost always encouraging them to ignore what actually matters - the specifics of the situation they are in.

Grappling is a continuous flow of movement and intentions; in order to be able to talk about it at all, let alone teach it, we need to break it up into discrete parts.  These parts form a language by which we can communicate with each other.  The basic unit of the BJJ vernacular is the Technique.


Technique definitions are based on large, relatively obvious limb positions.


 Back on the ground and legs around the opponent’s waist?  Closed Guard.

Legs around the opponent’s head and arm?  Triangle.

Kneeling with your opponent pulled across your shoulders, belly-down?  Kata-guruma.

There are variations and more details involved, but in the vast majority of cases, you can look at a photo and accurately determine the Techniques involved.

Black Belts learned to grapple in this language, and it is the one they use to describe their success.  When we go to the experts to learn, we naturally assume that they know what they are talking about, and that they know how best to talk about it in the first place.

One of the great strengths of this BJJ language is that it is easy to teach to beginners.  Because the definitions are static limb positions, it isn’t difficult to demonstrate them.  A day-one beginner may not be able to use Mount effectively, but you can put him in a recognizable Mount position with only a couple minutes work.

This ease-of-introduction is also strong evidence of a sub-optimal system.

The fact that you can put a novice into a recognizable Mount position is proof that “Mount” includes garbage jiu jitsu.
I have felt training partners give up a superior position in a scramble in order to solidify an inferior half-guard, because they believe that is where real BJJ happens.

Mount, Side Control, Arm Bars, Closed Guard – all of it can be performed terribly.
You might say that a garbage armbar is not a “true armbar”, like that Scotsman we’ve all heard about.  I understand that small nuances can make or break an arm(bar), but that is my point: we don’t use these small nuances to define an armbar, we use the large limb positions.  In fact, when applied under full-resistance, we find these nuances to be situation-dependent.

This is a feature of every Technique in BJJ – everything can be done poorly, and has some “invisible jiu jitsu”, some situation-specific modification, or just one last detail that the Professor will show you in class today.

I find the high number of unique specifics to be highly suspicious.  Every Technique has its own set of nuances (which change depending on the current conditions).  This gives the BJJ language a high level of irreducible complexity.  I believe that these nuances are like the [epicycles](https://en.wikipedia.org/wiki/Deferent_and_epicycle) added to geocentric planetary motion.  I think there is something else, some other dimension that is orthogonal to the [Flatland](https://en.wikipedia.org/wiki/Flatland) of BJJ.  If we center our understanding of grappling on something other than Technique, I think these epicycles will disappear.

